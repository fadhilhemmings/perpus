    private void TabelBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelBukuMouseClicked
        // TODO add your handling code here:
        int baris = TabelBuku.rowAtPoint(evt.getPoint());
        String Kode = TabelBuku.getValueAt(baris, 1).toString();
        txtKodeBuku.setText(Kode);
        
        String Judul = TabelBuku.getValueAt(baris, 2).toString();
        txtJudulBuku.setText(Judul);
        
        String Kategori = TabelBuku.getValueAt(baris, 3).toString();
        cbKategori.setSelectedItem(Kategori);
        
        String Tahun = TabelBuku.getValueAt(baris, 4).toString();
        cbTahunTerbit.setSelectedItem(Tahun);
        
        String Pengarang = TabelBuku.getValueAt(baris, 5).toString();
        txtPengarang.setText(Pengarang);
        
        String Penerbit = TabelBuku.getValueAt(baris, 6).toString();
        txtPenerbit.setText(Penerbit);
        
        String Stok = TabelBuku.getValueAt(baris, 7).toString();
        txtStockBuku.setText(Stok);
    }//GEN-LAST:event_TabelBukuMouseClicked

