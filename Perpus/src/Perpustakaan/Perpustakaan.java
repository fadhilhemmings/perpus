     private void tampilkan_data(){
        //membuat penamaan tabel di GUI tanpa mengedit
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Kode Buku");
        model.addColumn("Judul Buku");
        model.addColumn("Kategori");
        model.addColumn("Tahun Terbit");
        model.addColumn("Pengarang");
        model.addColumn("Penerbit");
        model.addColumn("Stok Buku");
        
        //kondisi agar kita bisa menampilkan data dengan try-catch
        try{
            //pengambilan data dari DB ke GUI
            int no = 1;
            String sql = "SELECT * FROM perpus";
            java.sql.Connection conn =(Connection)Konfig.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            //proses pemanggilan data dari DB ke GUI akan ditampilkan 
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),
                    res.getString(4),res.getString(5),res.getString(6),res.getString(7)}); 
            }
            TabelBuku.setModel(model);
            
        }catch (SQLException e){
            System.out.println("Error!"+ e.getMessage());
        } 
    }
    
    
